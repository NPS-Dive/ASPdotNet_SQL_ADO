
class Student
{

	 private   long  _ID ;   
	 private   string  _Name ;   
	 private   string  _Lastname ;   


 
	 public  long   ID {
		 get{  return _ID; }
		 set{_ID=  value;}
	}

 
	 public  string   Name {
		 get{  return _Name; }
		 set{_Name=  value;}
	}

 
	 public  string   Lastname {
		 get{  return _Lastname; }
		 set{_Lastname=  value;}
	}


}// End Class

